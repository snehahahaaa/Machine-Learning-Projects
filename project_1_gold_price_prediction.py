import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
from sklearn.model_selection import train_test_split
from sklearn.ensemble import RandomForestRegressor
from sklearn.metrics import r2_score

"""Data Collection & Processing"""

gold_data = pd.read_csv('/content/gld_price_data.csv')

#print first 5 rows of the dataframe
gold_data.head()

#print last 5 rows of the dataframe
gold_data.tail()

#num of rows and cols
gold_data.shape

#getting some basic infos about the data
gold_data.info()

#checking the num of missing values
gold_data.isnull().sum()

#getting the statistical measure of the data
gold_data.describe()

"""#Correlation
    1.Positive Correlation
    2.Negative Correlation

> Add blockquote


"""

correlation = gold_data.drop(columns=['Date']).corr()

#Constructing a heatmap to understand the correlation
plt.figure(figsize = (8,8))
sns.heatmap(correlation, cbar = True, square = True, fmt='.1f', annot= True, annot_kws={'size':8}, cmap= 'Reds')

#Correlation values of GLD
print(correlation['GLD'])

#Checking the distribution of the GLD price
sns.distplot(gold_data['GLD'], color ='purple')

"""Splitting the features & Target"""

X= gold_data.drop(['GLD', 'Date'], axis=1) #when dropping a col axis=1 and for row axis=0
Y= gold_data['GLD']

print(X)

print(Y)

"""Splitting into training data & test data"""

X_train, X_test, Y_train, Y_test = train_test_split(X, Y, test_size = 0.2, random_state = 2)

"""Model Training: Random Forest Regressor"""

regressor = RandomForestRegressor(n_estimators=100)

#training the model
regressor.fit(X_train, Y_train)

"""Model Evalution"""

#Prediction on test data
test_data_prediction = regressor.predict(X_test)

print(test_data_prediction)

#R squared error
error_score = r2_score(Y_test, test_data_prediction)
print("R squared error : ", error_score)

"""Compare the actual values & predicted values in a plot"""

Y_test = list(Y_test)

plt.plot(Y_test, color='blue', label = 'Actual Value')
plt.plot(test_data_prediction, color= 'green', label = 'Predicted Value')
plt.title('Actual Price vs Predicted Price')
plt.xlabel('Num of values')
plt.ylabel('GLD Price')
plt.legend()
plt.show